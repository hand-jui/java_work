package ch01;

public class Variable {

	public static void main(String[] args) {
		int age;
		int count;
		
		age=1;
		count=100;
		
		System.out.println(age);
		System.out.println("=====");
		System.out.println(count);
		
		age=500;
		count=900;
		
		System.out.println(age);
		System.out.println("=====");
		System.out.println(count);

		int myAge=29;
		System.out.println(myAge);
		System.out.println(27);
	}

}
